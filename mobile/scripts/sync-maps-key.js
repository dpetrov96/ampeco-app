/**
 * Syncs Google Maps API key for native builds and generates JS config.
 *
 * - Writes ios/Ampeco/GoogleMapsKey.generated.swift (runtime key for GMSServices)
 * - Keeps Info.plist GMSApiKey as a git-safe placeholder (never the real secret)
 * - Writes src/config/maps.generated.ts for JS provider selection
 * - Android still reads .env directly from Gradle
 *
 * `--clear` is a no-op for the Swift key (local builds keep working). It only
 * ensures Info.plist stays on the placeholder.
 */
const fs = require('fs');
const path = require('path');
const { execFileSync } = require('child_process');

const root = path.join(__dirname, '..');
const envPath = path.join(root, '.env');
const plistPath = path.join(root, 'ios/Ampeco/Info.plist');
const generatedTsPath = path.join(root, 'src/config/maps.generated.ts');
const generatedSwiftPath = path.join(
  root,
  'ios/Ampeco/GoogleMapsKey.generated.swift',
);
const PLACEHOLDER = 'YOUR_GOOGLE_MAPS_API_KEY';

function readMapsKey() {
  if (!fs.existsSync(envPath)) {
    return '';
  }

  const match = fs
    .readFileSync(envPath, 'utf8')
    .match(/^GOOGLE_MAPS_API_KEY=(.*)$/m);
  if (!match) {
    return '';
  }

  return match[1].trim().replace(/^['"]|['"]$/g, '');
}

function setPlist(command) {
  execFileSync('/usr/libexec/PlistBuddy', ['-c', command, plistPath]);
}

function writeSwiftKey(apiKey) {
  const escaped = apiKey.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
  fs.writeFileSync(
    generatedSwiftPath,
    [
      '// Generated by scripts/sync-maps-key.js — do not edit by hand.',
      '// Gitignored; created from mobile/.env (GOOGLE_MAPS_API_KEY).',
      'enum GoogleMapsKeyGenerated {',
      `  static let value: String = "${escaped}"`,
      '}',
      '',
    ].join('\n'),
  );
}

function setPlistPlaceholder() {
  const plistBuddy = '/usr/libexec/PlistBuddy';
  // Linux CI / non-mac hosts have no PlistBuddy — JS + Swift outputs are enough.
  if (
    process.platform !== 'darwin' ||
    !fs.existsSync(plistBuddy) ||
    !fs.existsSync(plistPath)
  ) {
    return;
  }

  try {
    setPlist(`Set :GMSApiKey ${PLACEHOLDER}`);
  } catch {
    try {
      setPlist(`Add :GMSApiKey string ${PLACEHOLDER}`);
    } catch {
      // Ignore — placeholder already present or plist not writable in this env.
    }
  }
}

const clear = process.argv.includes('--clear');
const keyFromEnv = readMapsKey();
const hasKey = Boolean(keyFromEnv);

// Never commit the real key via Info.plist — always placeholder there (macOS).
setPlistPlaceholder();

// Runtime key for the app binary (local / CI with .env).
writeSwiftKey(keyFromEnv);

fs.mkdirSync(path.dirname(generatedTsPath), { recursive: true });
fs.writeFileSync(
  generatedTsPath,
  `/* Generated by scripts/sync-maps-key.js — do not edit by hand. */\n` +
    `export const HAS_GOOGLE_MAPS_KEY = ${hasKey ? 'true' : 'false'} as const;\n`,
);

if (clear) {
  console.log(
    hasKey
      ? 'Info.plist kept placeholder; GoogleMapsKey.generated.swift has key from .env; HAS_GOOGLE_MAPS_KEY=true.'
      : 'Info.plist placeholder; no .env key — HAS_GOOGLE_MAPS_KEY=false.',
  );
} else if (!hasKey) {
  console.warn(
    'GOOGLE_MAPS_API_KEY missing — iOS will use Apple Maps; Android Google tiles may be blank.',
  );
} else {
  console.log(
    'Synced Google Maps key (GoogleMapsKey.generated.swift + maps.generated.ts).',
  );
}
